package com.plei.yang.pojo;
/**
 * 
 * @author pleiyang
 *顾客
 */
public class Customers {
	private String custId;//唯一的顾客id
	private String custName;//顾客姓名
	private String custAdderss;//顾客地址
	private String custCity;//顾客所在城市
	private String custSate;//顾客所在州
	private String custZip;//顾客地址邮政编码
	private String custCountry;//顾客所在国家
	private String custContact;//顾客的联系名
	private String custEmail;//顾客的电子邮件地址
	
	public String getCustId() {
		return custId;
	}
	public void setCustId(String custId) {
		this.custId = custId;
	}
	public String getCustName() {
		return custName;
	}
	public void setCustName(String custName) {
		this.custName = custName;
	}
	public String getCustAdderss() {
		return custAdderss;
	}
	public void setCustAdderss(String custAdderss) {
		this.custAdderss = custAdderss;
	}
	public String getCustCity() {
		return custCity;
	}
	public void setCustCity(String custCity) {
		this.custCity = custCity;
	}
	public String getCustSate() {
		return custSate;
	}
	public void setCustSate(String custSate) {
		this.custSate = custSate;
	}
	public String getCustZip() {
		return custZip;
	}
	public void setCustZip(String custZip) {
		this.custZip = custZip;
	}
	public String getCustCountry() {
		return custCountry;
	}
	public void setCustCountry(String custCountry) {
		this.custCountry = custCountry;
	}
	public String getCustContact() {
		return custContact;
	}
	public void setCustContact(String custContact) {
		this.custContact = custContact;
	}
	public String getCustEmail() {
		return custEmail;
	}
	public void setCustEmail(String custEmail) {
		this.custEmail = custEmail;
	}
	
	
}
